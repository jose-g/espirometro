package minsa.formulario.DataSet;

public class ConglomeradoDataSet {

    private String id;
    private String nombre;
    private String numero_puestos;
    private String tamano_muestral;
    private String ubigeo;
    private String direccion;
    private String latitud;
    private String longitud;
    private String id_tipo_conglomerado;
    private String eliminado;
    private String id_usuario;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getNumero_puestos() {
        return numero_puestos;
    }

    public void setNumero_puestos(String numero_puestos) {
        this.numero_puestos = numero_puestos;
    }

    public String getTamano_muestral() {
        return tamano_muestral;
    }

    public void setTamano_muestral(String tamano_muestral) {
        this.tamano_muestral = tamano_muestral;
    }

    public String getUbigeo() {
        return ubigeo;
    }

    public void setUbigeo(String ubigeo) {
        this.ubigeo = ubigeo;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getLatitud() {
        return latitud;
    }

    public void setLatitud(String latitud) {
        this.latitud = latitud;
    }

    public String getLongitud() {
        return longitud;
    }

    public void setLongitud(String longitud) {
        this.longitud = longitud;
    }

    public String getId_tipo_conglomerado() {
        return id_tipo_conglomerado;
    }

    public void setId_tipo_conglomerado(String id_tipo_conglomerado) {
        this.id_tipo_conglomerado = id_tipo_conglomerado;
    }

    public String getEliminado() {
        return eliminado;
    }

    public void setEliminado(String eliminado) {
        this.eliminado = eliminado;
    }

    public String getId_usuario() {
        return id_usuario;
    }

    public void setId_usuario(String id_usuario) {
        this.id_usuario = id_usuario;
    }

}