package minsa.formulario.DataSet;

public class TipConglomeradoDataSet {

    private String id;
    private String descripcion;
    private String habitacional_concurrencia;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getHabitacional_concurrencia() {
        return habitacional_concurrencia;
    }

    public void setHabitacional_concurrencia(String habitacional_concurrencia) {
        this.habitacional_concurrencia = habitacional_concurrencia;
    }

}